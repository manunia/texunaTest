package model;

public class Settings {
    private int width;
    private int height;
    private int number;
    private int date;
    private int fio;

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }

    public int getFio() {
        return fio;
    }

    public void setFio(int fio) {
        this.fio = fio;
    }
}
